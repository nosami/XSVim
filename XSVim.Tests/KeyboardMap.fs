namespace XSVim.Tests

open XSVim
module mapFromQwerty =
    let qwertyToColemak = function
        | 'e' -> 'f'
        | 'r' -> 'p'
        | 't' -> 'g'
        | 'y' -> 'j'
        | 'u' -> 'l'
        | 'i' -> 'u'
        | 'o' -> 'y'
        | 'p' -> ';'
        | 's' -> 'r'
        | 'd' -> 's'
        | 'f' -> 't'
        | 'g' -> 'd'
        | 'j' -> 'n'
        | 'k' -> 'e'
        | 'l' -> 'i'
        | ';' -> 'o'
        | 'n' -> 'k'
        | 'E' -> 'F'
        | 'R' -> 'P'
        | 'T' -> 'G'
        | 'Y' -> 'J'
        | 'U' -> 'L'
        | 'I' -> 'U'
        | 'O' -> 'Y'
        | 'P' -> ':'
        | 'S' -> 'R'
        | 'D' -> 'S'
        | 'F' -> 'T'
        | 'G' -> 'D'
        | 'J' -> 'N'
        | 'K' -> 'E'
        | 'L' -> 'I'
        | ':' -> 'O'
        | 'N' -> 'K'
        | c -> c

    let qwertyToDvorak = function
        | '-' -> '['
        | '=' -> ']'
        | 'q' -> '''
        | 'w' -> ','
        | 'e' -> '.'
        | 'r' -> 'p'
        | 't' -> 'y'
        | 'y' -> 'f'
        | 'u' -> 'g'
        | 'i' -> 'c'
        | 'o' -> 'r'
        | 'p' -> 'l'
        | '[' -> '/'
        | ']' -> '='
        | 's' -> 'o'
        | 'd' -> 'e'
        | 'f' -> 'u'
        | 'g' -> 'i'
        | 'h' -> 'd'
        | 'j' -> 'h'
        | 'k' -> 't'
        | 'l' -> 'n'
        | ';' -> 's'
        | ''' -> '-'
        | 'z' -> ';'
        | 'x' -> 'q'
        | 'c' -> 'j'
        | 'v' -> 'k'
        | 'b' -> 'x'
        | 'n' -> 'b'
        | ',' -> 'w'
        | '.' -> 'v'
        | '/' -> 'z'
        | '_' -> '{'
        | '+' -> '}'
        | 'Q' -> '\''
        | 'W' -> '<'
        | 'E' -> '>'
        | 'R' -> 'P'
        | 'T' -> 'Y'
        | 'Y' -> 'F'
        | 'U' -> 'G'
        | 'I' -> 'C'
        | 'O' -> 'R'
        | 'P' -> 'L'
        | '{' -> '?'
        | '}' -> '+'
        | 'S' -> 'O'
        | 'D' -> 'E'
        | 'F' -> 'U'
        | 'G' -> 'I'
        | 'H' -> 'D'
        | 'J' -> 'H'
        | 'K' -> 'T'
        | 'L' -> 'N'
        | ':' -> 'S'
        | '"' -> '_'
        | 'Z' -> ':'
        | 'X' -> 'Q'
        | 'C' -> 'J'
        | 'V' -> 'K'
        | 'B' -> 'X'
        | 'N' -> 'B'
        | 'M' -> 'M'
        | '<' -> 'W'
        | '>' -> 'V'
        | '?' -> 'Z'
        | c -> c

    let remap layout key =
        match layout with
        | Colemak -> qwertyToColemak key
        | Dvorak -> qwertyToDvorak key
        | _ -> key